class Solution {
  public:
    int canReach(int a[], int n) {
        // code here
        
       int goal=n-1;
       
       for(int i=n-2;i>=0;i--)
       {
           if( i+a[i]>=goal) goal=i;
       }
        
        return goal==0;
    }
};

